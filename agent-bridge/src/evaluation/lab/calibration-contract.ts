/**
 * A reviewable standard diff. Records what would change in the active
 * registry if a candidate were promoted.
 *
 *  - `diffId`: stable diff ID.
 *  - `candidateId`: the candidate being considered for promotion.
 *  - `additions`: standard entries that would be added to active.
 *  - `modifications`: standard entries that would replace existing active
 *    entries (same id, new version).
 *  - `removals`: standard entry IDs that would be removed from active
 *    (rare; only via explicit Robert instruction).
 *  - `beforeActiveFingerprint`: active registry fingerprint before.
 *  - `afterActiveFingerprint`: predicted active registry fingerprint after.
 *  - `integritySha256`: SHA-256 of the canonical form of this diff.
 */
export interface StandardDiff {
  schemaVersion: typeof STANDARD_DIFF_SCHEMA_VERSION;
  diffId: string;
  candidateId: string;
  additions: StandardEntry[];
  modifications: Array<{
    before: StandardEntry;
    after: StandardEntry;
  }>;
  removals: string[];
  beforeActiveFingerprint: string;
  afterActiveFingerprint: string;
  integritySha256: string;
}

