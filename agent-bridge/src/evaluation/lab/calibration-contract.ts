/**
 * The frozen list of ProjectFlow hard gates that ALWAYS take precedence
 * over semantic Judge evidence. Semantic results CANNOT offset these.
 */
export const FROZEN_HARD_GATES = [
  "state_invariant",
  "authority",
  "privacy_visibility",
  "proposal_confirm",
  "terminal_consistency",
  "idempotency",
  "forbidden_side_effect",
  "frozen_p0_gate",
] as const;

export type HardGateName = (typeof FROZEN_HARD_GATES)[number];

/**
 * Combine a hard-gate result with a semantic verdict. Hard-gate failure
 * ALWAYS wins. Semantic verdict can only downgrade, never upgrade.
 */
export function combineHardGateWithSemantic(
  hardGatePassed: boolean,
  semanticVerdict: SemanticVerdict,
): { finalVerdict: SemanticVerdict; rationale: string } {
  if (!hardGatePassed) {
    return {
      finalVerdict: "fail",
      rationale: "hard gate 失败，semantic 证据不能抵消",
    };
  }
  // Hard gate passed; semantic verdict stands.
  return {
    finalVerdict: semanticVerdict,
    rationale: "hard gate 通过，semantic verdict 生效",
  };
}
