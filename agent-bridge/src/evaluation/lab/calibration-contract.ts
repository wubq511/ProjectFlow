/**
 * A versioned standard entry. Lives in the active or candidate registry.
 *
 *  - `id`: stable standard ID (e.g., `semantic-rubric.planning-specificity`).
 *  - `kind`: discriminator for the typed payload.
 *  - `version`: monotonically increasing per-id version.
 *  - `fingerprint`: SHA-256 of the canonical form of the payload.
 *  - `payload`: typed payload matching `kind`.
 *  - `source`: where the standard came from (Git path + commit for active;
 *    calibration run ID + candidate ID for candidate).
 *  - `createdAt`: ISO timestamp.
 */
export interface StandardEntry {
  id: string;
  kind: StandardKind;
  version: number;
  fingerprint: string;
  payload: StandardPayload;
  source: {
    /** "active" for checked-in active standards; "candidate" for calibration outputs. */
    registry: "active" | "candidate";
    /** Git commit for active; calibration run ID for candidate. */
    origin: string;
    /** File path for active; artifact-relative path for candidate. */
    path: string;
  };
  createdAt: string;
}

export type StandardKind =
  | "semantic_rubric"
  | "semantic_anchor"
  | "scenario_version"
  | "judge_prompt_version"
  | "judge_model_compatibility"
  | "semantic_threshold"
  | "acceptance_proposal";

export type StandardPayload =
  | SemanticRubric
  | SemanticAnchorSet
  | ScenarioVersion
  | JudgePromptVersion
  | JudgeModelCompatibility
  | SemanticThreshold
  | AcceptanceProposal;

/**
 * A standards registry. Active and candidate registries use different
 * paths (see {@link StandardsRegistryRef}).
 */
export interface StandardsRegistry {
  schemaVersion: typeof STANDARDS_REGISTRY_SCHEMA_VERSION;
  /** "active" or "candidate". Active is read-only for normal eval. */
  registry: "active" | "candidate";
  /** Stable registry ID (e.g., "projectflow-active-v1"). */
  registryId: string;
  /** All entries in the registry. */
  entries: StandardEntry[];
  /** SHA-256 of the canonical form of the registry (entries sorted by id+version). */
  fingerprint: string;
  /** ISO timestamp of the last update. */
  updatedAt: string;
}

/** Reference to a registry by namespace + version + fingerprint. */
export interface StandardsRegistryRef {
  registry: "active" | "candidate";
  registryId: string;
  fingerprint: string;
  /** Optional Git commit (for active) or calibration run ID (for candidate). */
  origin?: string;
}

// ---------------------------------------------------------------------------
// §5 Criterion-scoped semantic rubric
// ---------------------------------------------------------------------------

/**
 * A criterion-scoped semantic rubric. Each rubric evaluates ONE criterion.
 *
 *  - `rubricId`: stable rubric ID.
 *  - `criterion`: the single criterion being evaluated (e.g.,
 *    "planning-specificity").
 *  - `label`: short human-readable label (Chinese).
 *  - `description`: what the Judge should look for (Chinese).
 *  - `scoreScale`: ordered score labels (e.g., ["poor", "fair", "good",
 *    "excellent"]). The Judge MUST pick one.
 *  - `evidenceReferences`: visible evidence references the Judge may use.
 *    Hidden CoT, evaluator oracle, raw private transcript, secrets and
 *    candidate model identity (when blindable) are FORBIDDEN.
 *  - `verdict`: the Judge's verdict.
 *  - `score`: the chosen score label.
 *  - `reason`: the Judge's reason (Chinese, must reference visible evidence).
 *  - `confidence`: Judge's confidence (0..1).
 *  - `judgeManifestRef`: reference to the Judge manifest used.
 *  - `rubricVersion`: schema version of this rubric.
 *  - `scenarioVersionRef`: scenario version this rubric applies to.
 *  - `anchorVersionRef`: anchor set version used for calibration.
 *  - `semanticHardGateEligible`: default `false`. Only `true` when the
 *    Judge has passed all stability tests AND the candidate standard
 *    has been promoted to active via explicit Robert instruction.
 */
export interface SemanticRubric {
  schemaVersion: typeof SEMANTIC_RUBRIC_SCHEMA_VERSION;
  rubricId: string;
  criterion: string;
  label: string;
  description: string;
  scoreScale: string[];
  evidenceReferences: Array<{
    /** Artifact-relative path or stable reference. */
    reference: string;
    /** SHA-256 of the referenced artifact (optional for live evidence). */
    referenceSha256?: string;
  }>;
  verdict: SemanticVerdict;
  score: string;
  reason: string;
  confidence: number;
  judgeManifestRef: {
    judgeId: string;
    judgeVersion: number;
  };
  rubricVersion: number;
  scenarioVersionRef?: {
    scenarioId: string;
    scenarioVersion: number;
  };
  anchorVersionRef?: {
    anchorSetId: string;
    anchorVersion: number;
  };
  semanticHardGateEligible: boolean;
}

// ---------------------------------------------------------------------------
// §6 Semantic anchors (good/bad/boundary)
// ---------------------------------------------------------------------------

/**
 * A semantic anchor set. Each set contains at least one good, one bad
 * and one boundary anchor for a specific criterion.
 *
 * Anchors are declared by versioned candidate standards. The Judge
 * cannot generate its own gold labels.
 */
export interface SemanticAnchorSet {
  schemaVersion: typeof SEMANTIC_ANCHOR_SCHEMA_VERSION;
  anchorSetId: string;
  criterion: string;
  version: number;
  anchors: SemanticAnchor[];
  /** Acceptance proposal for ordering stability. */
  acceptanceProposalRef: {
    proposalId: string;
    proposalVersion: number;
  };
}

export interface SemanticAnchor {
  anchorId: string;
  /** "good", "bad" or "boundary". Exactly one of these. */
  kind: "good" | "bad" | "boundary";
  /** The candidate output the Judge should evaluate. */
  output: string;
  /** Visible task facts the Judge may use (no hidden CoT, no oracle). */
  visibleFacts: string[];
  /** Expected ordering: good > boundary > bad. */
  expectedOrderRank: number;
  /** Optional expected verdict. */
  expectedVerdict?: SemanticVerdict;
  /** Optional expected score label. */
  expectedScore?: string;
}

