/**
 * The calibrate preset budget.
 *
 *  - `sut`: SUT (ProjectFlow Agent) cost ceiling. Capped at $3.
 *  - `evaluator`: independent ceiling for the Judge/simulator. Includes
 *    max calls, max input/output tokens, max wall time, max measurable
 *    dollars. Separate from SUT cap.
 *  - `codingAgent`: external/unknown, NOT counted against SUT cap.
 */
export interface CalibrateBudget {
  sut: {
    maxSutCostUsd: number;
    maxInputTokens: number;
    maxOutputTokens: number;
    maxRequestCount: number;
    maxWallTimeMs: number;
    maxObservations: number;
  };
  evaluator: {
    maxCalls: number;
    maxInputTokens: number;
    maxOutputTokens: number;
    maxWallTimeMs: number;
    maxMeasurableDollars: number;
  };
  codingAgent: {
    /** Always external/unknown for ProjectFlow eval. */
    costSource: "external" | "unknown";
  };
}

/** Cost provenance for a calibration run. */
