/**
 * T46-5 (Issue #98) — Calibration and Semantic Quality contracts.
 *
 * Additive extension over Slice 0 (`contract.ts`), Slice 1 (`contract-v2.ts`),
 * Slice 1 multi-turn (`contract-v3.ts`) and Slice 2 (`diagnosis-contract.ts`).
 * All V5 fields are OPTIONAL on existing artifacts so Slice 0/1/2 artifacts
 * continue to validate.
 *
 * Issue #98 acceptance criteria covered here:
 *  - Active vs Candidate standards registries (separate namespaces, no
 *    active write path from normal run / diagnose / repair-packet / Judge).
 *  - Standard conflicts across canonical docs, ADR, Pydantic/TypeScript
 *    schema, public behavior contract, current code behavior and frozen
 *    scenario/standard. Standard conflicts block promotion.
 *  - Criterion-scoped semantic rubric (one criterion at a time). Verdicts
 *    pass/fail/needs_review/infra_error/insufficient_evidence.
 *  - Blinding + random order + provenance for pairwise semantic evaluation.
 *  - Semantic anchors (good/bad/boundary) declared by versioned candidate
 *    standard, not generated by the Judge.
 *  - Judge stability tests (anchor ordering, A/B swap, verbosity, same-family
 *    preference, repeated-run, disagreement, unavailable, schema-invalid).
 *  - Judge fail-safe: 9 classes of degradation to `needs_review`.
 *  - Calibrate preset ($3 SUT cap + independent evaluator ceiling + cost
 *    provenance).
 *  - Calibration artifact (immutable, resumable, 17 required fields).
 *  - Promotion requires explicit Robert instruction; no cryptographic
 *    identity claim.
 *
 * Boundary invariants (must hold across all V5 modules):
 *  - ProjectFlow deterministic hard gates ALWAYS take precedence over
 *    semantic Judge evidence. Semantic results cannot offset hard-gate
 *    failures (state/authority/privacy/Proposal-Confirm/terminal/
 *    idempotency/forbidden side effect/frozen P0).
 *  - Semantic Judge verdicts default to `needs_review` whenever:
 *      (a) no independent Judge is available;
 *      (b) only same-family Judges are available and uncalibrated;
 *      (c) Judge identities cannot be confirmed;
 *      (d) Judges conflict;
 *      (e) anchor ordering is unstable;
 *      (f) position/verbosity/same-family bias exceeds candidate threshold;
 *      (g) Judge telemetry is incomplete;
 *      (h) Judge schema output is unrepairable;
 *      (i) calibration evidence is insufficient.
 *    Switching to a same-family Judge and continuing to issue a hard
 *    verdict is FORBIDDEN.
 *  - Active standards are check-in governed, versioned files. Normal run,
 *    diagnose, repair-packet and Judge paths have NO active write path.
 *    Calibration writes ONLY to candidate namespace.
 *  - Failed or unapproved calibration MUST leave the active registry
 *    byte-identical. No `active` mutation may occur without an explicit
 *    Robert instruction + reviewable Git diff + approval record.
 *  - Standard conflicts MUST be detected and recorded. Promotion is
 *    blocked while any `standard_conflict` remains unresolved. Current
 *    code behavior NEVER becomes truth merely because it exists.
 *  - Anchors MUST be declared by versioned candidate standards. The Judge
 *    cannot generate its own gold labels. A Judge that cannot stably
 *    distinguish anchors cannot become a hard gate.
 *  - `semantic_hard_gate_eligible` is a separate field, defaulting to
 *    `false`. A single successful Judge call never qualifies as
 *    stability evidence.
 *  - All calibration artifacts atomically enter the SHA-256 result graph.
 *  - Unsupported future schema versions fail-closed.
 *  - Cost provenance MUST be one of `provider_reported`,
 *    `versioned_price_estimate`, `unknown`. `unknown` cost MUST NOT be
 *    displayed as `$0`. Coding Agent cost stays `external/unknown` and
 *    is NOT counted against the SUT $3 cap.
 *  - Evaluator Judge/simulator cost lives under its OWN ceiling, separate
 *    from the SUT cap. Evaluator budget exhaustion stops NEW Judge calls
 *    but preserves completed evidence and partial artifacts.
 *  - Promotion never claims to cryptographically authenticate Robert's
 *    identity. It is repository governance with reviewable history.
 */

