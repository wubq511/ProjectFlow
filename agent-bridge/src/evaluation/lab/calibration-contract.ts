/**
 * A standard conflict. Recorded when canonical docs, ADRs, schemas,
 * public behavior contracts, current code behavior or frozen scenarios
 * disagree.
 *
 *  - `conflictId`: stable conflict ID.
 *  - `conflictingClaims`: the disagreeing claims, each with source
 *    type/version/hash.
 *  - `severity`: how serious the conflict is.
 *  - `resolutionStatus`: `unresolved` blocks promotion.
 *  - `affectedCandidateStandards`: which candidate standards are affected.
 *  - `detectedAt`: ISO timestamp.
 *  - `resolutionRationale`: only present when resolved/deferred.
 */
export interface StandardConflict {
  conflictId: string;
  conflictingClaims: Array<{
    claim: string;
    source: {
      type: StandardSourceType;
      /** Version label (e.g., "PRD-ProjectFlow-MVP.md@v1.2" or "commit abc123"). */
      version: string;
      /** SHA-256 of the source artifact (or "current" for live code behavior). */
      hash: string;
    };
  }>;
  severity: StandardConflictSeverity;
  resolutionStatus: StandardConflictResolution;
  affectedCandidateStandards: string[];
  detectedAt: string;
  /** Only present when resolutionStatus != "unresolved". */
  resolutionRationale?: string;
}

