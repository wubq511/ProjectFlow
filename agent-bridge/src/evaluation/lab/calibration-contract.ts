// §19 Fail-safe decision helpers
// ---------------------------------------------------------------------------

/**
 * Decide whether the Judge should degrade to `needs_review`.
 *
 * Returns the fail-safe reason when degradation is required; returns
 * `null` when the Judge can issue a verdict.
 *
 * This is the ONLY function the calibration runner uses to decide
 * fail-safe behavior. Adding a new fail-safe case requires extending
 * this function and its tests.
 */
export function decideJudgeFailSafe(input: {
  independentJudgeAvailable: boolean;
  judgeIdentityConfirmed: boolean;
  onlySameFamilyUncalibrated: boolean;
  judgesConflict: boolean;
  anchorOrderingUnstable: boolean;
  biasMetricsExceeded: boolean;
  judgeTelemetryIncomplete: boolean;
  judgeSchemaUnrepairable: boolean;
  calibrationEvidenceInsufficient: boolean;
}): string | null {
  if (!input.independentJudgeAvailable) {
    return "no_independent_judge";
  }
  if (!input.judgeIdentityConfirmed) {
    return "judge_identity_unconfirmed";
  }
  if (input.onlySameFamilyUncalibrated) {
    return "only_same_family_uncalibrated";
  }
  if (input.judgesConflict) {
    return "judges_conflict";
  }
  if (input.anchorOrderingUnstable) {
    return "anchor_ordering_unstable";
  }
  if (input.biasMetricsExceeded) {
    return "bias_metrics_exceeded";
  }
  if (input.judgeTelemetryIncomplete) {
    return "judge_telemetry_incomplete";
  }
  if (input.judgeSchemaUnrepairable) {
    return "judge_schema_unrepairable";
  }
  if (input.calibrationEvidenceInsufficient) {
    return "calibration_evidence_insufficient";
  }
  return null;
}

/** The 9 frozen fail-safe reasons. Adding a synonymous reason is forbidden. */
export const FROZEN_FAIL_SAFE_REASONS = [
  "no_independent_judge",
  "judge_identity_unconfirmed",
  "only_same_family_uncalibrated",
  "judges_conflict",
  "anchor_ordering_unstable",
  "bias_metrics_exceeded",
  "judge_telemetry_incomplete",
  "judge_schema_unrepairable",
  "calibration_evidence_insufficient",
] as const;

export type FailSafeReason = (typeof FROZEN_FAIL_SAFE_REASONS)[number];

