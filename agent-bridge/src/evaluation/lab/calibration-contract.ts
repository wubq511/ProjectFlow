/**
 * Frozen semantic verdicts. Exactly these 5 values are allowed.
 *
 *  - `pass`: the Judge evaluated the criterion and the evidence supports
 *    a positive verdict.
 *  - `fail`: the Judge evaluated the criterion and the evidence supports
 *    a negative verdict.
 *  - `needs_review`: the Judge could not produce a reliable verdict.
 *    Default for all fail-safe cases (see boundary invariants above).
 *  - `infra_error`: the Judge invocation itself failed (network, provider
 *    outage, schema unrepairable). Distinct from `needs_review` because
 *    the cause is infrastructure, not evidence.
 *  - `insufficient_evidence`: the Judge was invoked but the visible
 *    evidence was too sparse to support any verdict.
 *
 * Adding a synonymous verdict (e.g., "uncertain", "skipped") is forbidden.
 */
export type SemanticVerdict =
  | "pass"
  | "fail"
  | "needs_review"
  | "infra_error"
  | "insufficient_evidence";

export const FROZEN_SEMANTIC_VERDICTS: readonly SemanticVerdict[] = [
  "pass",
  "fail",
  "needs_review",
  "infra_error",
  "insufficient_evidence",
] as const;

/**
 * Frozen candidate standard promotion statuses. Exactly these 4 values
 * are allowed.
 *
 *  - `candidate`: produced by a calibration run, awaiting review.
 *  - `approved`: an explicit Robert instruction + reviewable diff has
 *    promoted the candidate to active. The approval record is immutable.
 *  - `rejected`: an explicit Robert instruction has refused promotion.
 *    The candidate stays in the candidate namespace for audit.
 *  - `superseded`: a newer candidate has replaced this one. The old
 *    candidate is preserved for rollback/audit but is no longer the
 *    latest proposal.
 *
 * Auto-promotion by an Agent, Judge or ordinary command is FORBIDDEN.
 */
export type CandidateStandardStatus =
  | "candidate"
  | "approved"
  | "rejected"
  | "superseded";

export const FROZEN_CANDIDATE_STATUSES: readonly CandidateStandardStatus[] = [
  "candidate",
  "approved",
  "rejected",
  "superseded",
] as const;

/**
 * Frozen standard conflict resolution statuses. Exactly these 3 values
 * are allowed.
 *
 *  - `unresolved`: the conflict is recorded but not yet resolved. Any
 *    promotion is blocked while at least one conflict affecting the
 *    candidate is unresolved.
 *  - `resolved`: an explicit Robert instruction has resolved the
 *    conflict. The resolution rationale is recorded.
 *  - `deferred`: an explicit Robert instruction has deferred the
 *    conflict (e.g., pending an ADR update). Promotion stays blocked.
 */
export type StandardConflictResolution =
  | "unresolved"
  | "resolved"
  | "deferred";

export const FROZEN_CONFLICT_RESOLUTIONS: readonly StandardConflictResolution[] = [
  "unresolved",
  "resolved",
  "deferred",
] as const;

/** Standard conflict severity. */
export type StandardConflictSeverity = "low" | "medium" | "high" | "critical";

