// ---------------------------------------------------------------------------
// §8 Pairwise evaluation record (blinding + random order + provenance)
// ---------------------------------------------------------------------------

/**
 * A pairwise semantic evaluation record. Records blinding, random order,
 * reverse-order repetition, disagreement and full provenance.
 */
export interface PairwiseEvaluationRecord {
  pairwiseId: string;
  /** The two candidates being compared. Identities are blinded when possible. */
  candidates: [
    { candidateLabel: "A" | "B"; candidateId: string; blinded: boolean },
    { candidateLabel: "A" | "B"; candidateId: string; blinded: boolean },
  ];
  /** The seed used to randomize A/B order (recorded in manifest). */
  seed: string;
  /** The actual display order chosen by the seed. */
  displayOrder: ["A", "B"] | ["B", "A"];
  /** Whether reverse-order repetition was performed. */
  reverseRepetition: boolean;
  /** The verdict from the forward order. */
  forwardVerdict: PairwiseVerdict;
  /** The verdict from the reverse order (if performed). */
  reverseVerdict?: PairwiseVerdict;
  /** Whether forward and reverse verdicts disagree. */
  disagreement: boolean;
  /** Judge provenance. */
  judgeManifestRef: {
    judgeId: string;
    judgeVersion: number;
  };
  /** Rubric, anchor and scenario versions. */
  rubricVersionRef: {
    rubricId: string;
    rubricVersion: number;
  };
  anchorVersionRef?: {
    anchorSetId: string;
    anchorVersion: number;
  };
  scenarioVersionRef?: {
    scenarioId: string;
    scenarioVersion: number;
  };
  /** Recorded blinding limitation (e.g., "candidate identity inferable from output style"). */
  blindingLimitation?: string;
  /** ISO timestamp. */
  evaluatedAt: string;
}

export type PairwiseVerdict =
  | { kind: "preference"; preferred: "A" | "B" | "tie"; confidence: number; reason: string }
  | { kind: "degraded"; verdict: SemanticVerdict; reason: string };

// ---------------------------------------------------------------------------
// §9 Judge bias metrics
// ---------------------------------------------------------------------------

/**
 * Bias metrics for a calibration run. Each metric carries the raw
 * measurements AND the candidate threshold so fail-safe decisions are
 * auditable.
 */
export interface JudgeBiasMetrics {
  /** Position bias: fraction of pairwise trials where the Judge preferred
   *  the first-displayed candidate, ignoring tie. */
  positionBias: {
    preferenceForFirst: number;
    sampleSize: number;
    candidateThreshold: number;
    exceeded: boolean;
  };
  /** Verbosity bias: correlation between output length and Judge score. */
  verbosityBias: {
    correlation: number;
    sampleSize: number;
    candidateThreshold: number;
    exceeded: boolean;
  };
  /** Same-family preference: when the Judge family matches one candidate's
   *  family, fraction of trials where the Judge preferred that candidate. */
  sameFamilyPreference: {
    preferenceRate: number;
    sampleSize: number;
    candidateThreshold: number;
    exceeded: boolean;
    /** Whether the Judge is same-family as any candidate. */
    sameFamilyDetected: boolean;
  };
  /** Disagreement rate: fraction of pairwise trials where forward and
   *  reverse order produced different verdicts. */
  disagreementRate: {
    rate: number;
    sampleSize: number;
    candidateThreshold: number;
    exceeded: boolean;
  };
  /** Repeated-run stability: fraction of repeated trials where the verdict
   *  flipped across repetitions. */
  repeatedRunInstability: {
    flipRate: number;
    sampleSize: number;
    candidateThreshold: number;
    exceeded: boolean;
  };
  /** Anchor ordering stability: fraction of anchor trials where the
   *  expected good > boundary > bad ordering was violated. */
  anchorOrderingStability: {
    violationRate: number;
    sampleSize: number;
    candidateThreshold: number;
    exceeded: boolean;
  };
}

